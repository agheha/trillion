package kr.co.momstudy.repository.vo;

public class Calenders {
	private int num;
	private int studyNo;
	private String title;
	private String content;
	private String startDate;
	private String endDate;
	private int type;
	private String ststart;
	private String stend;
	private String fontColor;
	private String barColor;
	private String email;
	
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getFontColor() {
		return fontColor;
	}
	public void setFontColor(String fontColor) {
		this.fontColor = fontColor;
	}
	public String getBarColor() {
		return barColor;
	}
	public void setBarColor(String barColor) {
		this.barColor = barColor;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public String getStstart() {
		return ststart;
	}
	public void setStstart(String ststart) {
		this.ststart = ststart;
	}
	public String getStend() {
		return stend;
	}
	public void setStend(String stend) {
		this.stend = stend;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getStudyNo() {
		return studyNo;
	}
	public void setStudyNo(int studyNo) {
		this.studyNo = studyNo;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}

	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
	
}
